#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	int		size;
	char	*str;

	if (!s)
		return (0);
	size = (int)ft_strlen(s);
	str = malloc((size + 1) * sizeof(char));
	if (!str)
		return (0);
	i = 0;
	while (i < size)
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
